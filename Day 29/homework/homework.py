# 1 დავალება
a = 5
b = 10
c = 15

result = (a < b) and (b < c) or (c < a)
print(result)

# ახსნა:
# (a < b) → True
# (b < c) → True
# (c < a) → False

# Logical ოპერატორების პრიორიტეტი:
# 1. and → True and True = True
# 2. or → True or False = True

# საბოლოოდ გამოაქვს True

# 2 დავალება

a = 5
b = 10
c = 15

# კოდი: (a < b) and (b < c) or (c < a)

# ჯერ ფასდება შედარებები (როგორც მათემატიკაში ჯერ ფრჩხილებია):
# a < b → 5 < 10 → True
# b < c → 10 < 15 → True
# c < a → 15 < 5 → False

#  შემდეგ ფასდება ლოგიკური ოპერატორები პრიორიტეტის მიხედვით
# (როგორც მათემატიკაში ჯერ გამრავლება, მერე მიმატება)

# and ხდება პირველი → True and True → True
# შემდეგ or → True or False → True

# ანუ საბოლოო შედეგია:
result = (a < b) and (b < c) or (c < a)

print(result)  # გამოიტანს: True

# პარალელი მათემატიკასთან:
# კოდი ჰგავს ასეთ მათემატიკურ მაგალითს:
# 2 + (3 * 4) → ჯერ ფრჩხილში (3 * 4 = 12), შემდეგ 2 + 12 = 14
# ანუ, პირველია ფრჩხილები და პრიორიტეტი → ისე როგორც ზემოთ კოდში.

